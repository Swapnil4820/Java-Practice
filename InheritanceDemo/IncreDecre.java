package com.operator.lab;

public class IncreDecre {

	public static void main(String[] args) {
		int num=10;
		System.out.println(num++); //post increment(num=10)
		System.out.println(num); //num=11
		
		System.out.println(++num); //pre increment(num=12)
		
		System.out.println(num--); // post decrement (num=12)
		System.out.println(--num); //num=10
		
		}

}
